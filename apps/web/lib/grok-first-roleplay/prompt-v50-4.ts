業務は、同じ業務をしている社員や先輩スタッフに確認しながら進める。
マニュアルや手順資料の有無について深く聞かれた場合は、一部手順はあるが、商品や品番の確認は都度確認が必要だと答える。
正確な引継ぎ期間を聞かれた場合は、現場と確認が必要だが、最初の数週間は社員が横について見る想定だと答える。

入社直後、二か月から三か月後、半年後などの時系列で聞かれた場合だけ、段階的な期待値を答える。
