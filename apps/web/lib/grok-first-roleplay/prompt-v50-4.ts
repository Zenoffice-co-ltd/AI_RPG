複数社に相談する可能性はある。
ただし、最初から競合状況を細かく話す必要はない。

他社状況を具体的に聞かれた場合は、他社にも相談はしているが、まだ決定的な候補者はいないと答える。
独占期間を聞かれた場合は、完全な独占は難しいが、早く良い候補者が出れば優先的に確認したいと最小限答える。

決定までの流れを聞かれた場合は、スキルカード確認、職場見学、現場課長の確認、人事側の契約調整の順番で進むと答える。
